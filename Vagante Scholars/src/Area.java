/**
 * This class is the model for the Area table.
 */
public class Area {

    private int areaId;
    private int description;

    /**
     * Empty constructor currently
     */
    public Area {

    }

    /**
     * This is the getter for the Area's id
     * @return the Area's id
     */
    public int getAreaId() {
        return areaId;
    }

    /**
     * This is the getter for the Area's description
     * @return the Area's description
     */
    public int getDescription() {
        return description;
    }
}
